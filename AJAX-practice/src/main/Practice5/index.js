
function browseLink(){
    var id= document.getElementById('button-link').value;
    if(id==='Google.com'){
        window.open('https://www.google.com','_blank');

    }
    else
    if(id==='Yahoo.com'){
        window.open('https://www.yahoo.com','_blank');

    }
    else
    if(id==='Facebook.com'){
        window.open('https://www.facebook.com','_blank');

    }
    else
    if(id==='Upgrad.com'){
        window.open('https://www.upgrad.com','_blank');

    }
}